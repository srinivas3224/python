my_sum = lambda arg1, arg2: arg1 + arg2
my_sub = lambda arg1, arg2: arg1 - arg2
my_mul = lambda arg1, arg2: arg1 * arg2
my_div = lambda arg1, arg2: arg1 / arg2


# Now you can call sum as a function
print ("Value of total : ", my_sum( 10, 20 ))
print ("Value of sub : ", my_sub( 200, 20 ))
print ("Value of Multiplication : ", my_mul( 15, 20 ))
print ("Value of Division : ", my_div( 200, 20 ))
